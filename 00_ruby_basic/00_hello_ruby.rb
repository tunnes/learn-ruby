# Using 'puts' to print an message in console:
puts "Hello Ruby"

# To concatenate an ruby comand inside an String:
puts "1 + 2 = #{1 + 2}"

# Ruby primitive types:
# In ruby all is an object provided by any class, to check the object class type use '.class' properity.
puts "An String".class  # String
puts 'a'.class          # String
puts 40.class           # Integer
puts 4.0.class          # Float
puts true.class         # TrueClass (Boolean)
puts false.class         # FalseClass (Boolean)

# Variables:
var01 = 1
puts var01.class